package 과제.과제5_개인;


public class Member {
    
    // 클래스
    // 필드
    String 아이디;
    String 비밀번호;
    String 이름;
    String 전화번호;
    String 나이;
    
    //생성자
    Member(String 아이디,String 비밀번호, String 이름, String 전화번호 ,String 나이){
        this.아이디 = 아이디;
        this.비밀번호 = 비밀번호;
        this.이름 = 이름;
        this.전화번호 = 전화번호;
        this.나이 = 나이;

    }
}
